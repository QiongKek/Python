# ---type(数据)
# ---检测数据类型

# int --整型
num1 = 1
print(type(num1))

# float -- 浮点型
num2 = 1.1
print(type(num2))

# str -- 字符串，特点：数据都要带引号
a = 'hello world'
print(type(a))

# bool -- 布尔型，通常判断使用，布尔型由两个取值
b = True
print(type(b))

# list -- 列表
c = [10, 20, 30]
print(type(c))
print(c)

# tuple -- 元组
d = (10, 20, 30)
print(type(d))
print(d)

# set --集合
e = {10, 20, 30}
print(type(e))
print(e)

# dict -- 字典 -- 键值对
f = {'name': 'Tom', 'age': 18}
print(type(f))



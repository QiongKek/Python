"""
break:终⽌此循环
continue:退出当前⼀次循环继⽽执⾏下⼀次循环代码
"""
# 如果吃的过程中，吃完第三个吃饱了，则不需要再吃第4个和第五个苹果
i = 1
while i <= 5:
    if i == 4:
        print(f'吃饱了不吃了')
        break
    print(f'吃了第{i}个苹果')
    i += 1

print()
# ：如果吃的过程中，吃到第三个吃出⼀个⼤⾍⼦...,是不是这个苹果就不吃了，开始吃第四个苹果
j = 1
while j <= 5:
    if j == 3:
        print(f'⼤⾍⼦，第{j}个不吃了')
# 在continue之前⼀定要修改计数器，否则会陷⼊死循环
        j += 1
        continue
    print(f'吃了第{j}个苹果')
    j += 1




